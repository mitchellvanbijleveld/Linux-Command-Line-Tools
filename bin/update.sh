#!/bin/bash
VAR_UTILITY="bin"
VAR_UTILITY_SCRIPT="update"
VAR_UTILITY_SCRIPT_VERSION="2024.XX.XX-XXXX"
VAR_SCRIPT_REQUIRED_COMMAND_LINE_TOOLS="git PrintMessage"

PrintMessage "INFO" "$VAR_UTILITY" "$VAR_UTILITY_SCRIPT" "Using command 'git' to pull the latest version..."
VAR_OLD_VERSION=$(cat "$VAR_BIN_INSTALL_DIR/VERSION")
git -C "$VAR_BIN_INSTALL_DIR" pull
VAR_NEW_VERSION=$(cat "$VAR_BIN_INSTALL_DIR/VERSION")
PrintMessage "INFO" "$VAR_UTILITY" "$VAR_UTILITY_SCRIPT" "Done pulling from git."

PrintMessage "INFO" "$VAR_UTILITY" "$VAR_UTILITY_SCRIPT" "OLD VERSION: $VAR_OLD_VERSION"
PrintMessage "INFO" "$VAR_UTILITY" "$VAR_UTILITY_SCRIPT" "NEW VERSION: $VAR_NEW_VERSION"